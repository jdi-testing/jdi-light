package com.epam.jdi.light.ui.bootstrap.elements.complex;

import com.epam.jdi.light.elements.base.UIBaseElement;
import com.epam.jdi.light.elements.complex.JList;
import com.epam.jdi.light.elements.pageobjects.annotations.locators.UI;
import com.epam.jdi.light.ui.bootstrap.elements.common.Progress;

import java.util.List;
import java.util.stream.Collectors;

public class MultipleProgressBars extends UIBaseElement {

    @UI(".progress-bar")
    private JList<Progress> barsList;

    public JList<Progress> getProgresses() {
        return barsList;
    }

    public List<String> getValues() {
        return barsList.stream().map(Progress::getValue).collect(Collectors.toList());
    }

    public Progress getProgress(int index) {
        return barsList.get(index);
    }
}
